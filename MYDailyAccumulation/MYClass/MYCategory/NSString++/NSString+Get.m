#pragma mark - 拼音首字母
- (NSString *)getFirstLetter {
    
    //转成了可变字符串
    NSMutableString *str = [NSMutableString stringWithString:self];
    //先转换为带声调的拼音
    CFStringTransform((CFMutableStringRef)str,NULL, kCFStringTransformMandarinLatin,NO);
    //再转换为不带声调的拼音
    CFStringTransform((CFMutableStringRef)str,NULL, kCFStringTransformStripDiacritics,NO);
    //转化为大写拼音
    NSString *strPinYin = [str capitalizedString];
    //获取并返回首字母
    return [strPinYin substringToIndex:1];
}

#pragma mark - 拼音首字母
- (NSString *)firstPinyin
{
    if ([self length] == 0) {
        return [NSString stringWithFormat:@"%c",'['];
    }
    
    if ([self canBeConvertedToEncoding:NSASCIIStringEncoding]) {
        return [self substringToIndex:1];
    }
    
    unichar ch = [self characterAtIndex:0];
    char firstPinYin = ch;
    if (ch < 'A' || ch > 'z' || (ch > 'Z' && ch < 'a') ) {
        firstPinYin = pinyinFirstLetter(ch);
    }
    
    return [NSString stringWithFormat:@"%c",firstPinYin];
}

